# Script para explicar el uso de los operadores matemáticos

# Define variables
a = 7
b = 4
c = 2

# Imprime algunas operaciones matemáticas

print ("Imprime una suma")
print (a + c)

print ("\nImprime una resta")
print (a - b)

print ("\nImprime una multiplicación")
print (b * c)

print ("\nImprime una división")
print (a / c)

print ("\nImprime la división redondeada a un entero")
print (a // c)

print ("\nImprime el uso de varios operadores")
print ((a + b) * c)
print ((a * b) / (2 * a))

print("\nImprime el residuo")
print (b % c)
print (a % b)

print ("\nImprime la potencia de un número")
print (c ** b)
